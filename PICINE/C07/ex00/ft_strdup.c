#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int	ft_strlen(char *s1)
{
	int	i;

	i = 0;
	while (s1[i] != '\0')
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	char	*new;
	int		i;

	i = 0;
	new = (char *)malloc(sizeof(char) * ft_strlen(src) + 1);
	if (!(new))
		return ("\0");
	while (src[i])
	{
		new[i] = src[i];
		i++;
	}
	new[i] = ('\0');
	return (new);
}
